package CUUDUONGTHANCONG.Entity;

public class Menu {
	private int idmenu;
	private String name;
	private String url;
	public Menu() {
		super();
	}
	public int getIdmenu() {
		return idmenu;
	}
	public void setIdmenu(int idmenu) {
		this.idmenu = idmenu;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
