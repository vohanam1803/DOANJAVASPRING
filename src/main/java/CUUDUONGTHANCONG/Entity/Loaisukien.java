package CUUDUONGTHANCONG.Entity;

public class Loaisukien {
	private int id;
	private String name;
	private String mota;
	public Loaisukien() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMota() {
		return mota;
	}
	public void setMota(String mota) {
		this.mota = mota;
	}
}
